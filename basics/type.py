# Integer Types
student_id = 31
print("Id: ", student_id)
print("Type: ", type(student_id))

# String types
student_name = "Jamal"
print("Name: ", student_name)
print("Type:", type(student_name))

# Float type
height = 180.5
print("Height: ", height)
print("Type: ", type(height))

# Type conversation (integer)
student_year = "4"
x = int(student_year)
print("Year: ", x)
print("Type: ", type(x))

# Conver to float
y = float(student_year)
print("Float type: ", type(y))
